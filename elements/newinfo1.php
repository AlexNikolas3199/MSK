			<p>Матвиенко: российские специалисты провели реставрацию купола Капитолия в Гаване на высочайшем уровне</p>
		</div>
		<p class="newText">Российские специалисты, занимавшиеся реставрацией купола Капитолия
в Гаване, выполнили работы на высочайшем уровне, сказала спикер Совета
Федерации Валентина Матвиенко в пятницу на встрече с главой
Нацассамблеи народной власти и Госсовета страны Эстебаном Ласо
Эрнандесом в кубинской столице.
«Это, действительно, уникальный пример высочайшего профессионализма
реставраторов», — подчеркнула Матвиенко. О помощи в проведении
реставрационных работ кубинская сторона попросила шесть лет назад в ходе
предыдущего визита председателя Совета Федерации в Гавану. «Мы как
ответственные люди сразу отреагировали и посчитали это хорошей идеей»,
— отметила она.
По словам спикера, специалисты из России выполнили непростую и
уникальную работу, причём реставрацию удалось завершить к дню
празднования 500-летия Гаваны. «Это подарок России нашему
дружественному кубинскому народу, подарок столице Кубы в честь её
юбилея», — заявила Матвиенко, добавив, что Москва и Гавана должны
продолжать активно сотрудничать.
Здание кубинского Капитолия было открыто в 1929 году при президенте
Херардо Мачадо и до 1950-х годов было самым высоким сооружением
кубинской столицы. До победы революции в 1959 году в Капитолии заседал
парламент страны. Затем здание использовалось в качестве конгресс-центра
и было открыто для посетителей. В качестве части архитектурного ансамбля
Старой Гаваны в 1982 году Капитолий включён в Список Всемирного
наследия ЮНЕСКО; в 2010 году — объявлен монументом национального
значения.
Завершение работ в Капитолии приурочено к 500-летию празднования со дня
основания Гаваны, которое будет отмечаться 16 ноября. В торжественной
церемонии открытия отреставрированного здания в субботу примет участие
Валентина Матвиенко.</p>
	    
