		<p class="newText">Архитектурные, исторические и археологические памятники Соловецкого поселка...
"За что они не возьмутся, все у них выходит удачно, все ведется со знанием дела, упорно, неотступно, пока полный успех не увенчает их усилий."
Василий Немирович-Данченко. Соловки. 1880
 Здание монастырской электростанции
Здание монастырской электростанции входит в список "Памятников поселка Соловецкий". Тип памятника - электростанция. До последнего времени её техническое состояние оценивалось как руинированное. Идут работы по восстановлению как самого здания, так и силовой установки. Существует устойчивый миф о том, что Соловецкая монастырская гидроэлектростанция была первой в России.
"Именно тут, на одном из островов, впервые в России появилась электростанция" - считают многие (Ирина Маланина. Первая электростанция появилась в монастыре. Щит и меч, Москва. 27.03.2003.) Это расхожее мнение - одна из легенд Соловков. Первая в России электростанция была построена в Санкт-Петербурге на ул. Новгородской российско–немецко–бельгийским обществом «Гелиос» в 1897 году, а Соловецкая электростанция стала первой в регионе и единственной электростанцией в хозяйстве русских монастырей. Впрочем, если И.Маланина имела ввиду именно островную электростанцию, то она безусловно права. 
Электростанция СЛОНа
1923 год: "Электростанция еще ремонтировалась, света не было." (Ширяев Борис. Неугасимая лампада. Нью-Йорк: Изд-во им. Чехова, 1954. 413 с.)
 "Когда меня вторично привезли на Соловки, я жил в Кремле. Вначале я попал на работу на дровяной склад, на вылавливание бревен (баланов) из моря и доставку их к электростанции. Если бы я работал в этом качестве и далее, то вскоре был бы мне конец, но судьба сжалилась, я был переведен на заготовку дров для котельной, где дрова распиливались и кололись для топки котлов. Вскоре меня «присмотрел» заведующий электростанцией Овсяный Иван Семенович и перевел меня поммашиниста (масленщиком) в машинный зал, а затем и своим помощником — механиком. В этой должности с весны 1934 года я проработал до лета 1938 года...
Живя при станции мы (Овсяный и я), были заняты практически круглые сутки. Забот хватало. Надо было бесперебойно обеспечивать электроэнергией Кремль, управление и близлежащие к Кремлю постройки. Я занимался ремонтом и профилактикой оборудования: котлов, паровых машин, механизмов котельной (насосы и инжектора), трубопровода для пара и воды, электромоторов для привода пилы и станков в ремонтном цеху, водяной турбины. Можно было помыться после смены. Все эти сносные условия заставляли держаться за эту работу, работали все честно, с полной отдачей. В итоге это сохраняло жизнь.
