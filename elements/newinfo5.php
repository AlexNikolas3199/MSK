Здание монастырской электростанции входит в список "Памятников поселка Соловецкий". Тип памятника - электростанция. До последнего времени её техническое состояние оценивалось как руинированное. Идут работы по восстановлению как самого здания, так и силовой установки. Существует устойчивый миф о том, что Соловецкая монастырская гидроэлектростанция была первой в России.
"Оставшиеся полуразрушенные стены из красного, я бы сказал Соловецкого кирпича, зданием назвать сложно, но всё-таки отдельные элементы конструкции сохранились достаточно хорошо и чётко указывают на своё первоначальное предназначение, сохранилась прямоугольная металлическая, на заклёпках труба, по которой вода подавалась к турбине. 
    
Внешний вид Соловецкой электростанции. Фото неизвестного автора из альбома, подаренного соловецкими чекистами Сергею Кирову. Соловки, СЛОН. 20-е годы XX века.
На другой фотографии начала 20-х годов (?) прошлого века видна металлическая опора под козырьком и далее деревянные опоры воздушных линий электропередач, которые расходятся от электростанции. Видны поднимающиеся в верх клубы пара и две дымовые трубы, а с правой стороны здания по углам установлены молниеприёмники. С увелиением количества потребителей электроэнергии в 1925 году была произведена реконтрукция электростанции.
Соловецкая электростанция
Далее уже при входе в здание, эта труба переходит в трубу круглого сечения, которая соединяется с гидравлической турбиной Френсиса, на валу которой сохранились два маховика. Турбина носит имя британского инженера-гидротехника Джеймса Френсиса, который в 1833 году переехал в Америку.
На некотором расстоянии от этих маховиков и параллельно им, сохранился фундамент, металлеческая плита, на которой, видимо, стоял генератор. Вращение от маховиков передавалось, предположительно, посредством ременной передачи.
Если смотреть на здание электростанции со стороны сухого дока (с лицевой стороны), то чётко видна металлическая решётчатая башня, с четырёхскатным небольшим козырьком на верху, над которой возвышается маленький флаг-флюгер. На этой опоре сохранились крюки под изоляторы, к которым крепились отходящие воздушные линии электропередачи.
Интересно то, что на фотографии запечатлевшей электростанцию, сделанной русским фотографом Сергеем Прокудиным-Горским в 1912 году этой металлической конструкции не видно. Возникает вопрос, каким образом в те времена шло распределение электрической энергии потребителям? По кабельным линиям? Вместе с тем на фотографии Прокудина-Горского видна дымовая труба с растяжками, стоящая возле здания электростанции. Это подтверждает тот факт, что электрическая энергия вырабатывалась не только гидрогенератором, но и паровыми машинами.
 
Опора Соловецкой электростанции. Вид со стороны внутренних помещенй станции. Фото Игоря Кондаурова, Соловки. 2007.
Мощность электростанции на момент запуска была 145 л.с., вырабатывался постоянный ток напряжением 220 вольт. Один генератор приводился в действие гидравлической турбиной Френсиса мощностью 60 л.с., а два других - паровыми машинами мощностью 60 и 25 л.с. Основная нагрузка электростанции была силовой и только 14% - осветительной.
 
Фрагмент с крюками для электрических изоляторов опоры Соловецкой электростанции: вид со стороны дока. Фото Игоря Кондаурова, Соловки. 2007.
Электростанция была спроектирована Петербургским отделением Акционерного общества русских электрических заводов "Сименс и Гальске". Кроме основного гидрогенератора мощностью 60 л.с., проектом были предусмотрены резервные пародинамомашины мощностью 60 и 25 л.с. Строительные материалы были изысканы на месте, работы выполнены монастырскими работниками и трудниками под наблюдением инженеров. Акционерное общество Русских заводов и фирма «Пирвиц и К» в Риге приняли на себя доставку на место и установку всех механизмов, гарантировали годичные испытания работоспособности машин.
Электростанция была пущена в работу 27 сентября 1912 года. Заведовал электростанцией и лесопильным заводом электротехник Илья Иванович Варакин. Приёмку электростанции произвели эксперты: директор Архангельского технического училища имени императора Петра 1 инженер-электрик В.Н.Пономарёв, техник Архангельского торгового порта инженер-механик Д.Н.Любавин в присутствии заведующего электростанцией при доме коммерческого собрания в Архангельске К.А.Неймана и Иероманаха Викториана. 
Нельзя говорить, что Соловецкая электростанция была первой в России. В 1887 году вступила в строй Царскосельская центральная электрическая станция переменного (однофазного) тока. В 1900 году в Петербурге было 294 частных и центральных электростанций, в столице было около 300 электрических станций. (Кондауров Игорь. Соловецкая электростанция: строительство, работа и разрушение. На правах рукописи. Одесса. 2007. Под. ред. Юрия Серова. СоловкиЭнциклопедия, 2007.)
