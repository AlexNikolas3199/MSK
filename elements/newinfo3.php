		<p class="newText">5 декабря премьер-министр России Дмитрий Медведев дал большое ежегодное интервью телеканалам, в рамках которого затронул тему восстановления Соловецкого монастыря. 
«Есть проект, в котором мы в попечительском совете с Патриархом Кириллом принимаем участие, — это Соловки. Это по-своему уникальное место для нашей страны, оно и мемориальное, и религиозное, и, конечно, восстановить его — наша задача», — заявил премьер-министр.
Глава Правительства отметил: «Все еще усложняется тем, что помимо монастыря там есть населенный пункт, который нужно приводить в порядок. Это достаточно сложный и затратный процесс, но я уверен, что мы все сделаем. Это духовная опора нашей страны».
</p>
