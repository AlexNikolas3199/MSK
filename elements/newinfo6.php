		<p class="newText">Начиная с 1962 года Соловецкий музей-заповедник проводит планомерную реставрацию архитектурных памятников Соловецкого Кремля, построенного в конце XVI века….
В 1992 году Соловецкий историко-культурный комплекс был внесен в список памятников Всемирного наследия ЮНЕСКО. Восстановительные работы на Соловках, где также действует Соловецкий государственный историко-архитектурный и природный музей-заповедник, имеют неоценимое значение для сохранения духовного, историко-культурного и природного наследия России и осуществляются при поддержке государства.
В это благое дело международного значения вносит существенный вклад компания «МСК», выполняя работы по  корректировке и доработке научно-проектной документации по реставрации и приспособлению к современному использованию объектов культурного наследия федерального значения: «Северный келейный корпус, 1859-1865 годы, Голгофо-Распятский скит, XIX век», «Восточный келейный корпус, XIX в.», «Здание монастырской электростанции, 1910-1912годы,1920-1930 годы».
