	<div class="tsunamitext">
		<p>На каждом этапе работ над проектом производится его стоимостная оценка позволяющая корректировать стоимость реализации и принять наиболее эффективное проектное решение.<br><br> Архитектурно-строительное проектирование зданий, сооружений и других строительных объектов начинается с проведения инженерно-изыскательных работ . Проведение таких работ необходимо, в первую очередь, на новых земельных участках, где ни разу не проводилась разведка грунта, почвы и грунтовых вод.<br><br> Перечень изыскательных работ обговаривается с каждым заказчиком индивидуально, но мы рекомендуем провести комплексные работы, которые помогут сделать прогноз изменений земельного участка в будущем. В результате заказчик получит список подробных рекомендаций. И уже после проведения инженерно-изыскательных работ сможет без особых финансовых и технических рисков приступать к этапу проектирования здания или сооружений.<br><br> Состав проектной документации определяется Градостроительным Кодексом РФ и Постановлением Правительства Российской Федерации от 16.02.2008 № 87-ФЗ «О составе разделов проектной документации и требования к их содержанию».<br><br> Разработанная нашими специалистами проектная документация будет полностью отвечать всем нормам, правилам и стандартам. Мы можем выполнить проектирование как в один, так и в два этапа. Количество этапов проектирования полностью зависит от сложности объекта и пожеланий заказчика.</p>
