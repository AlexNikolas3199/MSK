			<p>29.10.2019</p>
			<p class="line">——</p>
			<p>Президент Кубы в Москве вручил медали Дружбы реставраторам купола Капитолия в Гаване</p>
		</div>
		<p class="newText">Награды удостоились 52 россиянина
МОСКВА, 29 октября. /ТАСС/. Президент Кубы Мигель Диас-Канель во вторник в ходе торжественной церемонии в Москве вручил кубинские медали Дружбы российским специалистам, которые участвовали в реставрации купола гаванского Капитолия.
Как сообщил в ходе церемонии посол Кубы в РФ Херардо Пеньяльвер Порталь, награды удостоились 52 россиянина, а также два предприятия из России.
"Этот замечательный коллектив архитекторов, реставраторов и художников из России определенный период жили как кубинцы, в нашей действительности, работая очень усиленно". - сказал Порталь после вручения медалей.
Кубинцы теперь принимают российских специалистов "как своих сыновей", подчеркнул президент.
Как отметил посол республики, работа реставраторов из России имеет исключительное историческое, художественное и культурное значение. "Она, несомненно, будет содействовать укреплению дружеских связей между двумя народами и правительствами", - добавил он.
Российские реставраторы в конце августа завершили работы по восстановлению золотого покрытия купола гаванского Капитолия. Они также работали и над восстановлением статуи Республики внутри здания Капитолия, в открытии которой принял участие глава МИД РФ Сергей Лавров во время своего визита на остров в конце июля.
Кубинский Капитолий был построен в 1929 году и до 1950-х годов был самым высоким зданием в Гаване. До победы революции в 1959 году в нем заседал парламент страны. Затем здание использовалось в качестве конгресс-центра и было открыто для посетителей.
Своим внешним обликом здание напоминает вашингтонский Капитолий, однако не является его репликой. Гаванский Капитолий имеет также сходство с парижским Пантеоном и Собором Святого Петра в Риме.
